# Crea un proyecto Plantilla de Python que disponga de un archivo requirements.txt y un .venv que pueda ser ejecutado desde Visual Studio Code

# Creamos el entorno virtual con python -m venv <nombre-del-entorno>
# En mi caso el entorno se llama ejercicios_python
# Activamos el entorno con source ejercicios_python/bin/activate
# Creamos un archivo requirements.txt con un par de dependecias "cualquiera" para este ejercicio.
# Instañamos el requirements con pip install -r requirements.txt desde la carpeta en la que lo tenemos
# Al ser un entorno nuevo, al instala requirements.txt me dice que hay una nueva versión de pip y puedo hacer upgrade
# Actualizamos y volvemos a instalar requirements. para confirmar que está todo ok y todos salen "requirements already satisfied"