# Insertar la primera variable
numero_1 = float(input("Por favor, inserte el primer número: "))

# Insertas la segunda variable
numero_2 = float(input("Por favor, inserte el segundo número: "))

# Calcular la suma de los dos números
resultado = numero_1 + numero_2

# Imprimir el resultado
print(f"La suma de {numero_1} y {numero_2} es: {resultado}")