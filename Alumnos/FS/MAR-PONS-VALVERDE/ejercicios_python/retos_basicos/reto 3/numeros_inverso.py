# Escribe un programa que sea capaz de mostrar los números del 1 al 100 en orden inverso

for numero in range(100, 0, -1):
    print (numero)

# utilizamos la función range() para generar una secuencia de números desde 100 hasta 1 (excluyendo el 0) con un paso de -1, 
# lo que significa que iremos restando 1 en cada iteración. 
# Luego, utilizamos un bucle for para recorrer esta secuencia de números y los imprimimos en orden inverso.
